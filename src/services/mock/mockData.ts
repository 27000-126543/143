const sensorThresholds: Record<string, { warning: number; danger: number; unit: string }> = {
  temperature: { warning: 35, danger: 40, unit: '℃' },
  humidity: { warning: 80, danger: 90, unit: '%' },
  methane: { warning: 0.5, danger: 1.0, unit: '%LEL' },
  hydrogenSulfide: { warning: 10, danger: 20, unit: 'ppm' },
  liquidLevel: { warning: 50, danger: 80, unit: 'cm' },
  waterImmersion: { warning: 1, danger: 2, unit: '级' },
};

